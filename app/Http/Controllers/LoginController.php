    {
        $check1=[
            'email'=> $request->username, 
            'password'=> $request->password, 
        ];
        $check2=[
            'name'=> $request->username, 
            'password'=> $request->password, 
        ];
        if (auth()->attempt($check1)||auth()->attempt($check2)) {
            return redirect()->route('home');
        }

        return back()->withErrors([
            'message' => '帳號或密碼錯誤'
        ]);
    }

